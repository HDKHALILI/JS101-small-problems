// List of Digits
// Write a fucntion that takes one argument, a positive integer, and returns a
// list of digits in the numbers.

// Examples:

// digitList(12345);       // [1, 2, 3, 4, 5]
// digitList(7);           // [7]
// digitList(375290);      // [3, 7, 5, 2, 9, 0]
// digitList(444);         // [4, 4, 4]

function digitList(integers) {
  let result = String(integers).split('').map(integer => Number(integer));

  return result;
}

console.log(digitList(12345));       // [1, 2, 3, 4, 5]
console.log(digitList(7));           // [7]
console.log(digitList(375290));      // [3, 7, 5, 2, 9, 0]
console.log(digitList(444));         // [4, 4, 4]